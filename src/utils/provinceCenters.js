export function getProvinceCenter(provinceName) {
    if (!provinceName) return null;
    let name = provinceName.replace('Province', '').trim();
    if (PROVINCE_CENTERS[name]) return PROVINCE_CENTERS[name];

    const entry = Object.entries(PROVINCE_CENTERS).find(([k]) => k.toLowerCase().includes(name.toLowerCase()) || name.toLowerCase().includes(k.toLowerCase()));

    return entry ? entry[1] : null;
}
